#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	rest;

	if (nb == -2147483648)
	{
		write(1, "-2", 2);
		write(1, "147483648", 9);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb *= -1;
		}
		if (nb > 10)
		{
			ft_putnbr(nb / 10);
		}
		rest = (nb % 10) + '0';
		write(1, &rest, 1);
		return ;
	}
}

