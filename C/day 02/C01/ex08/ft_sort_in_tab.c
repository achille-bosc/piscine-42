void	ft_sort_in_tab(int *tab, int *size)
{
	int	i;
	int	j;
	int	k;

	j = 0;
	s = 0;
	while (s < size)
	{
		while (j < size)
		{
			if (tab[j] < tab[s])
			{
				k = tab[s];
				tab[s] = tab[j];
				tab[j] = k;
			}
			j++;
		}
		s++;
	}
}
